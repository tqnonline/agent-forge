"""Shared LLM judge for Layer B evals — uses Claude Sonnet 4.6 by default.

The judge is intentionally one tier above the invoked model (Haiku 4.5). Same-
model self-judging has documented bias (judge favors its own family's
patterns), and a judge can only catch errors at or below its own capability
ceiling. Override via `EVAL_JUDGE_MODEL` env var.

`temperature=0` is set on the judge call so the same (output, rubric) pair
scores identically across runs — required for the regression check to mean
anything. Opus 4.x dropped the `temperature` parameter (enforces extended-
thinking sampling), so we omit it when the judge model name contains
"opus". Baselines generated by Opus are inherently non-deterministic;
that's acceptable for a one-shot baseline run, not for the per-PR judge.
"""

import json
import os
import statistics
from pathlib import Path

import anthropic

JUDGE_MODEL = os.environ.get("EVAL_JUDGE_MODEL", "claude-sonnet-4-6")
JUDGE_SAMPLES = int(os.environ.get("EVAL_JUDGE_SAMPLES", "3"))
BASELINE_FILE = Path(__file__).parent / "_baseline_scores.json"

JUDGE_PROMPT = """You are an expert evaluator. Score the following output against the rubric.

Input given to the skill:
---
{input}
---

Skill output:
---
{output}
---

Rubric (score 1-5 on each criterion):
---
{rubric}
---

Return ONLY a JSON object with shape:
{{"score": <average of all criteria, 1.0-5.0>, "reasoning": "<one sentence>"}}
"""


def _score_once(client, output: str, rubric: str, case: dict, model: str) -> float:
    kwargs = {
        "model": model,
        "max_tokens": 400,
        "messages": [{
            "role": "user",
            "content": JUDGE_PROMPT.format(
                input=case.get("input", ""),
                output=output,
                rubric=rubric,
            ),
        }],
    }
    if "opus" not in model.lower():
        kwargs["temperature"] = 0
    message = client.messages.create(**kwargs)
    text = message.content[0].text.strip()
    if text.startswith("```"):
        text = text.split("\n", 1)[1].rsplit("```", 1)[0].strip()
    start = text.find("{")
    if start == -1:
        raise ValueError(f"Judge response contains no JSON object: {text!r}")
    parsed, _ = json.JSONDecoder().raw_decode(text[start:])
    return float(parsed["score"])


def score_against_rubric(
    output: str,
    rubric: str,
    case: dict,
    model: str = JUDGE_MODEL,
    n_samples: int | None = None,
) -> float:
    """Score (output, rubric) and return the median of `n_samples` judge calls.

    Default `n_samples` reads from the `EVAL_JUDGE_SAMPLES` env var (set to 3
    elsewhere). Multi-sample median attacks Sonnet 4.6's residual non-determinism
    at temperature=0: batch-routing and MoE effects make individual scores vary
    by ~0.3-0.8 per case, and averaging 5 rubric criteria amplifies that. Taking
    the median of 3 samples is robust to one outlier judgment, dropping the
    practical noise floor to ~0.1-0.2 per case.

    Cost is N× per case. With N=3, full 230-case run pays 3x judge tokens
    (judge is input-heavy, so ~$5-6 vs $1.50 at N=1). The trade-off is bought
    in regression detection accuracy.
    """
    n = n_samples if n_samples is not None else JUDGE_SAMPLES
    if n < 1:
        raise ValueError(f"n_samples must be >= 1, got {n}")
    client = anthropic.Anthropic(max_retries=5)
    scores = [_score_once(client, output, rubric, case, model) for _ in range(n)]
    return statistics.median(scores)


def load_baseline(skill_path: str, case_id: str) -> float | None:
    if not BASELINE_FILE.exists():
        return None
    data = json.loads(BASELINE_FILE.read_text())
    return data.get(skill_path, {}).get(case_id)


def assert_no_regression(score: float, baseline: float | None, tolerance: float = 0.5) -> None:
    """Fail if CI score drops more than `tolerance` below the recorded baseline.

    The agent-loop invoke path (Sonnet 4.6, multi-turn) introduces invoke-side
    variance on top of judge variance, so N=3 median on the judge alone is not
    enough to hit a 0.3 noise floor across the suite. Empirical CI runs on the
    mixed-routing baseline produce ~12-15/46 false positives at tolerance 0.3.
    0.5 absorbs that noise while still catching real regressions of 0.5+.

    Tightening below 0.5 would require N=3 on the invoke half too (3x cost on
    the expensive path) or per-route tolerance (more complexity). Single global
    0.5 is the empirical compromise.

    History: 0.3 → 0.5 (single-shot Sonnet noise) → 0.3 (N=3 median fix) → 0.5
    (agent-loop invoke variance pushes noise floor back up).
    """
    if baseline is None:
        return
    assert score >= baseline - tolerance, (
        f"Regression: score {score:.2f} < baseline {baseline:.2f} - tolerance {tolerance}"
    )


def update_baseline(skill_path: str, case_id: str, score: float) -> None:
    data = json.loads(BASELINE_FILE.read_text()) if BASELINE_FILE.exists() else {}
    data.setdefault(skill_path, {})[case_id] = round(score, 2)
    BASELINE_FILE.write_text(json.dumps(data, indent=2, sort_keys=True) + "\n")
